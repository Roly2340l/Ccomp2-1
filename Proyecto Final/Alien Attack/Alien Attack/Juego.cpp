using namespace std;


Juego::Juego(int ancho, int alto, string titulo)
{
	fps = 60;

	//DECLARACION DE LA VENTANA
	ventana = new RenderWindow(VideoMode(ancho, alto), titulo);
	ventana->setFramerateLimit(fps);

	//DECLARACION DEL PERSONAJE (NOTA: PASAR A UNA CLASE)
	textura = new Texture;
	textura->loadFromFile("Imagenes/bola.png");

	sprite = new Sprite;
	sprite->setTexture(*textura);
	sprite->setScale(16.f / sprite->getTexture()->getSize().x, 16.f / sprite->getTexture()->getSize().y); //Reducir el tamaño del sprite
	
	//MAPA-FONDOS (NOTA: PASAR A UNA CLASE)
	fondo1 = new Texture;
	fondo1->loadFromFile("Imagenes/cerros.png");

	nivel1 = new Sprite;
	nivel1->setTexture(*fondo1);
	nivel1->setScale((float)(1088.f / nivel1->getTexture()->getSize().x), (float)(768.f / nivel1->getTexture()->getSize().y));
	
	tierra = new Texture;
	tierra->loadFromFile("Imagenes/tile_00.png");

	pasto = new Texture;
	pasto->loadFromFile("Imagenes/tile_04.png");

	bloque1 = new Sprite;
	bloque1->setTexture(*tierra);

	bloque2 = new Sprite;
	bloque2->setTexture(*pasto);

	//LLAMA A LA FUNCIÓN DEL BUCLE
	repetir();
}
void Juego::dibujar()
{
	//COLOR DE LA PANTALLA
	ventana->clear();

	//DIBUJA EL MAPA Y FONDOS
	ventana->draw(*nivel1);

	//DIBUJA EL SPRITE DEL PERSONAJE (NOTA: PASAR A UNA CLASE)
	ventana->draw(*sprite);

	//MUESTRA LA PANTALLA


}

